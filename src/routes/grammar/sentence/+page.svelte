			<hr/>

			<p>
				Another of the first particle to learn, is the <span class="font-sealang">V</span>.
				Placed right after the verb, it turn the sentence into negation. For example the previous sentence :
				"<span class="font-sealang">eA?fD??</span>"(I have a fish) become "<span class="font-sealang">eA?V ?fD??</span>" (I have no fish)
