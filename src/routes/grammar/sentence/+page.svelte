				<br/>
				To differentiate a word usage, we use something called a "particle". For example the verb "<span class="font-sealang">s</span>" (live)
				become a noun with the noun particle  "<span class="font-sealang">D</span>" : "<span class="font-sealang">sD</span>" (alive)
				<br/>
				Verbs use 3 different particles to conjugate themself, past, present and futur : "<span class="font-sealang">E</span>", "<span class="font-sealang">A</span>" and "<span class="font-sealang">C</span>".
